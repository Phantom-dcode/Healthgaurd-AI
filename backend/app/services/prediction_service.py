"""
app/services/prediction_service.py
─────────────────────────────────────────────────────────────────
AI risk prediction using Scikit-Learn.

Approach:
  Phase 6 uses a LogisticRegression model trained on synthetic
  data that mirrors real clinical risk patterns. In production,
  replace synthetic data with real patient records.

  The model is trained ONCE on startup (if not cached) and saved
  to disk with joblib. Subsequent startups load from disk.

  Feature vector (8 features):
    [age, weight_kg, systolic_bp, diastolic_bp,
     heart_rate, blood_sugar, oxygen_level, temperature]
─────────────────────────────────────────────────────────────────
"""
import logging
import uuid
from datetime import date
from pathlib import Path
from typing import Optional

import numpy as np
import joblib
from sqlalchemy.orm import Session

from app.models.patient       import Patient
from app.models.health_record import HealthRecord
from app.models.prediction    import Prediction, RiskLevel

logger       = logging.getLogger(__name__)
MODEL_PATH   = Path("app/ml_model/health_risk_model.pkl")
MODEL_VERSION = "1.0.0"

# ── Risk score boundaries ─────────────────────────────────────
HIGH_THRESHOLD   = 0.60
MEDIUM_THRESHOLD = 0.30


def _score_to_level(score: float) -> RiskLevel:
    if score >= HIGH_THRESHOLD:
        return RiskLevel.high
    if score >= MEDIUM_THRESHOLD:
        return RiskLevel.medium
    return RiskLevel.low


# ── Model Training ───────────────────────────────────────────
def _train_model():
    """
    Train a LogisticRegression model on synthetic health data.
    Labels are generated by a deterministic risk-scoring function
    that mirrors clinical guidelines.
    """
    logger.info("Training risk prediction model with synthetic data…")
    np.random.seed(42)
    N = 2000

    age        = np.random.randint(20, 85, N).astype(float)
    weight     = np.random.uniform(40, 160, N)
    sys_bp     = np.random.randint(70, 210, N).astype(float)
    dia_bp     = np.random.randint(40, 140, N).astype(float)
    hr         = np.random.randint(35, 160, N).astype(float)
    sugar      = np.random.uniform(50, 450, N)
    o2         = np.random.uniform(82, 100, N)
    temp       = np.random.uniform(34.0, 41.5, N)

    X = np.column_stack([age, weight, sys_bp, dia_bp, hr, sugar, o2, temp])

    # Deterministic labelling based on clinical rules
    y = []
    for i in range(N):
        risk = 0
        if sys_bp[i] >= 180 or sys_bp[i] < 85:    risk += 3
        elif sys_bp[i] >= 140:                      risk += 2
        elif sys_bp[i] >= 130:                      risk += 1

        if sugar[i] > 400 or sugar[i] < 55:         risk += 3
        elif sugar[i] > 200 or sugar[i] < 70:        risk += 2
        elif sugar[i] > 140:                          risk += 1

        if o2[i] < 90:                               risk += 4
        elif o2[i] < 95:                             risk += 2

        if hr[i] > 150 or hr[i] < 40:               risk += 3
        elif hr[i] > 100 or hr[i] < 55:             risk += 1

        if temp[i] > 39.5 or temp[i] < 35.0:        risk += 2
        elif temp[i] > 38.0:                          risk += 1

        if age[i] > 65:                              risk += 1

        y.append(2 if risk >= 6 else (1 if risk >= 3 else 0))

    from sklearn.linear_model  import LogisticRegression
    from sklearn.preprocessing import StandardScaler
    from sklearn.pipeline      import Pipeline

    pipeline = Pipeline([
        ("scaler", StandardScaler()),
        ("clf",    LogisticRegression(multi_class="ovr", max_iter=1000,
                                       class_weight="balanced", random_state=42)),
    ])
    pipeline.fit(X, y)

    MODEL_PATH.parent.mkdir(parents=True, exist_ok=True)
    joblib.dump(pipeline, MODEL_PATH)
    logger.info("Model trained and saved to %s", MODEL_PATH)
    return pipeline


# ── Model Loading ─────────────────────────────────────────────
_model = None  # module-level singleton


def get_model():
    global _model
    if _model is None:
        if MODEL_PATH.exists():
            _model = joblib.load(MODEL_PATH)
            logger.info("Model loaded from disk: %s", MODEL_PATH)
        else:
            _model = _train_model()
    return _model


# ── Feature Extraction ────────────────────────────────────────
def _extract_features(patient: Patient, record: HealthRecord) -> tuple[np.ndarray, dict]:
    """
    Build the feature vector from patient profile + latest record.
    Missing values are replaced with healthy-range defaults.
    Returns (ndarray shape [1,8], feature_dict for snapshot).
    """
    # Compute age from date_of_birth
    today = date.today()
    dob   = patient.date_of_birth
    age   = (today - dob).days / 365.25 if dob else 45.0

    features = {
        "age":         round(float(age), 1),
        "weight_kg":   float(record.weight_kg   or 70.0),
        "systolic_bp": float(record.systolic_bp  or 120),
        "diastolic_bp":float(record.diastolic_bp or 80),
        "heart_rate":  float(record.heart_rate   or 72),
        "blood_sugar": float(record.blood_sugar  or 100.0),
        "oxygen_level":float(record.oxygen_level or 98.0),
        "temperature": float(record.temperature  or 36.6),
    }

    X = np.array([[
        features["age"],
        features["weight_kg"],
        features["systolic_bp"],
        features["diastolic_bp"],
        features["heart_rate"],
        features["blood_sugar"],
        features["oxygen_level"],
        features["temperature"],
    ]])
    return X, features


# ── Public API ────────────────────────────────────────────────
def predict_risk(
    patient: Patient,
    record:  HealthRecord,
    db:      Session,
) -> Prediction:
    """
    Run the ML model and persist the Prediction row.
    Returns the saved Prediction ORM object.
    """
    model         = get_model()
    X, feat_snap  = _extract_features(patient, record)

    # Model returns 0=low, 1=medium, 2=high
    proba         = model.predict_proba(X)[0]   # shape [3]
    label_idx     = int(np.argmax(proba))
    risk_score    = float(proba[label_idx])
    risk_level    = [RiskLevel.low, RiskLevel.medium, RiskLevel.high][label_idx]

    prediction = Prediction(
        patient_id       = patient.patient_id,
        record_id        = record.record_id,
        risk_score       = round(risk_score, 4),
        risk_level       = risk_level,
        model_version    = MODEL_VERSION,
        feature_snapshot = feat_snap,
    )
    db.add(prediction)
    db.commit()
    db.refresh(prediction)

    logger.info(
        "Prediction created",
        extra={
            "patient_id": str(patient.patient_id),
            "risk_level": risk_level.value,
            "risk_score": risk_score,
        },
    )
    return prediction
